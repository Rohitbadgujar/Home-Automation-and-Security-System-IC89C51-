class MyTimerTask extends TimerTask {

        @Override
        public void run() {
            if (!running) {
                return;
            }
            if (parent.dataReady) {
                parent.dataReady = false;
                if (currentChannel == 0) {
                    System.out.println("Curr" + currentChannel + " Received : " + parent.inData);
                }
                //jProgressBar1.setValue(parent.inData);
                adcValues[currentChannel] = parent.inData;
                jp[currentChannel].setValue(parent.inData);
                currentChannel++;
                if (currentChannel == 8) {
                    currentChannel = 0;
                }
            }
            parent.writeData(73);
            parent.writeData(currentChannel);
            try {
                Thread.sleep(25);
            } catch (InterruptedException ex) {
                Logger.getLogger(TestSensorFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
   
